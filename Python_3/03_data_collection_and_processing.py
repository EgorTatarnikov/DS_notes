# Ğ‘ĞµĞ· zip â€” Ğ²Ñ€ÑƒÑ‡Ğ½ÑƒÑ:
def manual_zip(list1, list2):
    result = []
    for i in range(min(len(list1), len(list2))):
        result.append((list1[i], list2[i]))
    return result

